package lv.kristaps.tactictool;

public class Image {
    public String UserID, ImageName, TacticName;

    public Image() {

    }


    public String getUserID() {
        return UserID;
    }

    public String getImageName() {
        return ImageName;
    }

    public String getTacticName() {
        return TacticName;
    }

    public void setUserID() {
        this.UserID = UserID;
    }

    public void setTacticName() { this.TacticName = TacticName; }

    public void setImageName() { this.ImageName = ImageName; }
}
